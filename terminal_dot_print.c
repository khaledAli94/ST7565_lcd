void framebuffer_to_page_major_rev(
    const uint8_t *src,
    uint8_t *dst
) {
    for (int page = 0; page < 8; page++) {

        int hw_page = 7 - page;   // <-- reverse page order

        for (int x = 0; x < 128; x++) {
            uint8_t byte = 0;

            for (int bit = 0; bit < 8; bit++) {
                int y = page * 8 + bit;
                int src_index = y * 128 + x;

                uint8_t pixel = (src[src_index / 8] >> (src_index % 8)) & 1;
                byte |= pixel << bit;
            }

            dst[hw_page * 128 + x] = byte;
        }
    }
}


void framebuffer_to_page_major(
    const uint8_t *src,   // 1024‑byte linear framebuffer
    uint8_t *dst          // 1024‑byte page‑major output
) {
    for (int page = 0; page < 8; page++) {
        for (int x = 0; x < 128; x++) {
            uint8_t byte = 0;

            for (int bit = 0; bit < 8; bit++) {
                int y = page * 8 + bit;
                int src_index = y * 128 + x;

                uint8_t pixel = (src[src_index / 8] >> (src_index % 8)) & 1;
                byte |= pixel << bit;
            }

            dst[page * 128 + x] = byte;
        }
    }
}

