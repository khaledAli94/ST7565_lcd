void printBufferAsDots() {
    for (int y = 0; y < LCD_HEIGHT; y++) {
        int page = y / 8;
        int bit  = y % 8;

        for (int x = 0; x < LCD_WIDTH; x++) {
            uint8_t byte = st7565_buffer[page * LCD_WIDTH + x];
            if (byte & (1 << bit))
                printf("#");
            else
                printf(".");
        }
        printf("\n");
    }
}

